age=85         #你可以改变这个值，测试程序是否能争取进入该去的循环
if(age <= 2):
    print('Hi, you are a infant.')
elif(age <= 4):
    print('Hi, you are a baby.')
elif(age <= 13):
    print('Hi, you are a child.')
elif(age <= 20):
    print('Hi, you are a teenager.')
elif(age <= 65):
    print('Hi, you are an adult.')
else:
    print('Hi, you are an elderly person. Thanks for your hard work.')